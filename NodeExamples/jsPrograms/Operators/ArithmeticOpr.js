//Arithmetic operators (+, -, *, /, %, **, ++, --)
var x = 12;
//pre-Increment: return the value of a variable after it has been incremented.
console.log(++x); //13
console.log(x); //13
//post-Increment: return the value of a variable before it has been incremented.
console.log(x++); //13
console.log(x); //14

console.log(x ** 2); //exponential(square)
console.log(Math.pow(9, 3)); //here two statements produce the same result
console.log(x/4);
console.log(x%3);
console.log(x + 4 * 4 / 2);
console.log((x + 4)* 4 / 2);